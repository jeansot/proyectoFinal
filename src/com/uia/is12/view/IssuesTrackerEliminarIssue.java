    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            IssuesBL bl = new IssuesBL();
            Issues is = new Issues();
            is.setId(Integer.parseInt(txtId.getText()));
            bl.eliminarProyecto(is);
            JOptionPane.showMessageDialog(null, "Issue eliminado");
            txtId.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(IssuesTrackerEliminarIssue.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(IssuesTrackerEliminarIssue.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

