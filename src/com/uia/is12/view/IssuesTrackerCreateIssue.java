    private void btGenerarIssueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGenerarIssueActionPerformed
        Issues isu = new Issues();
        IssuesBL bl = new IssuesBL();
        isu.setId(Integer.parseInt(txtId.getText()));
        isu.setNombre(txtNombreDelIssue.getText());
        isu.setDescripcion(txtDescripcion.getText());
        int prioridad=this.jComboPrioridad.getSelectedIndex();
        if (prioridad==0) {
            String prioridadA = "Alta";
            isu.setPrioridad(prioridadA);
        }else{
            if (prioridad==1) {
                String prioridadA = "Media";
                isu.setPrioridad(prioridadA);
            }else{
                if (prioridad==2) {
                    String prioridadA = "Baja";
                    isu.setPrioridad(prioridadA);
                }
            }
            int privacidad = this.jComboBox2.getSelectedIndex();
            if (privacidad==0) {
            String privacidadA = "Publico";
            isu.setPrivacidad(privacidadA);
        }else{
            if (privacidad==1) {
                String privacidadA = "Privado";
                isu.setPrivacidad(privacidadA);
            }else{
                if (prioridad==2) {
                    String privacidadA = "Baja";
                    isu.setPrivacidad(privacidadA);
                }
            }
           
        }
            if (tbUserName.getSelectedRow() != -1) {
            username = tbUserName.getModel().getValueAt(tbUserName.getSelectedRow(), 0).toString();
            JOptionPane.showMessageDialog(null, username);
            isu.setAsignar(username);
        }
            
        try {
            bl.agregarIssue(isu);
            JOptionPane.showMessageDialog(null, "Issue agregado\nNombre del Issue:"+isu.getNombre()+"\nAsignado a:"+isu.getAsignar());
            txtId.setText("");
            txtDescripcion.setText("");
            txtNombreDelIssue.setText("");
            
        } catch (SQLException ex) {
            Logger.getLogger(IssuesTrackerCreateIssue.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_btGenerarIssueActionPerformed

