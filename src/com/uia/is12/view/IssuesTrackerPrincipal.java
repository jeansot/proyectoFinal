    private void btMostrarDatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btMostrarDatosActionPerformed
        // TODO add your handling code here:
         UserBL bl=new UserBL();
        User usuario=new User();
        UserDAO dao = new UserDAO();
        
        
        DefaultTableModel modelo=new DefaultTableModel();
        this.tbMostrarDatos.setModel(modelo);
        try {
            ResultSet rs=dao.MostrarUsuarios(usuario);
            
            ResultSetMetaData rsm=rs.getMetaData();
            int colum=rsm.getColumnCount();
            
            for(int i=1;i<=colum;i++){
                modelo.addColumn(rsm.getColumnLabel(i));
            }
            
            while(rs.next()){
                Object [] fila=new Object[colum];
                
                for(int i=0;i<colum;i++){
                    fila[i]=rs.getObject(i+1);
                    
                }
                modelo.addRow(fila);
                }
                
                
            }catch (SQLException ex) {
                 Logger.getLogger(IssuesTrackerPrincipal.class.getName()).log(Level.SEVERE, null, ex);
             }
            
    }//GEN-LAST:event_btMostrarDatosActionPerformed

