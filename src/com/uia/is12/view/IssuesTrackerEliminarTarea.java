    private void btEliminarTareaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEliminarTareaActionPerformed
        try {
            // TODO add your handling code here:
            TaskBL bl = new TaskBL();
            Tarea task = new Tarea();
            task.setId(Integer.parseInt(txtId.getText()));
            bl.eliminarUsuario(task);
            JOptionPane.showMessageDialog(null, "Tarea eliminada");
            txtId.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(IssuesTrackerEliminarTarea.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(IssuesTrackerEliminarTarea.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btEliminarTareaActionPerformed

